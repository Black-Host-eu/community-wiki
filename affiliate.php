   Wie funktioniert die Auszahlung
  </h1>

  <div class="py-5">

    Eine Auszahlung kannst du im Support anfragen. Dies wird benötigt um "Betrüger", die Aufrufe über andere Server generieren,
    nicht die Möglichkeit zu geben das Geld auszahlen zu lassen. Vor der Auszahlung erfolgt eine automatisierte Prüfung deiner Aufrufe.


