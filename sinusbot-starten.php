        <h5>Möglichkeit 1</h5>
        <br><p>Schritt 1: Zuerst musst du dich in das Sinusbot Interface einloggen. Solltest du nicht wissen, wie das geht, schau in unseren Wiki Artikel.</p>
        <br><p>Schritt 2: Du musst in der Navbar unter Settings und dann Instances gehen. Dort werden alle deine Instanzen gelistet.</p>
        <br><p>Schritt 3: Dann musst du bei der gewünschten Instanz auf den Start/Stop Button klicken (Siehe Bild) damit startest oder stopst den Bot.</p><br><img width="1100" src="https://cdn.jonas-budde.de/screenshots/6vh8KiSsYdyTo8Apx4D6KtdhXPpmG4Lg.png"><br>
        <br>
        <h5>Möglichkeit 2</h5>
        <br><p>Schritt 1: Zuerst musst du dich im Sinusbot Interface einloggen. Zuerst musst du dich in das Sinusbot Interface einloggen. Solltest du nicht wissen, wie das geht, schau in unseren Wiki Artikel.</p>
        <br><p>Schritt 2: Du hast oben in der Navbar eine Auswahl zwischen den Instanzen (Ist nur da, wenn auch eine zweite Instanz erstellt ist). Dort kannst du die gewünschte Instanz auswählen, wenn nur eine Instanz existiert, brauchst du keine Auswählen.</p><br><img src="https://cdn.jonas-budde.de/screenshots/4Rc3f8Dgu5vt9EHMMb4NY923tZ0o49Xj.png"><br>
        <br><p>Schritt 3: Dann musst du nur noch in der Navbar auf den Start/Stop Button klicken (Siehe Bild) damit startest oder stoppst den Bot.</p><br><img src="https://cdn.jonas-budde.de/screenshots/YNtKPkuAEFlKnziKW8gCjbDSa4b2yCrs.png">

