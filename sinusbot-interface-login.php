        <p>Schritt 1: Zuerst musst du dich bei uns im <a href="https://game.host-control.eu/" target="_blank">Gameserver Interface</a> anmelden, um dir die IP Adresse und den Port von deinem Sinusbot zu kopieren.</p>
        <br><p>Schritt 2: Dort musst du dir dann die IP Adresse und den Port kopieren. Da solltest du nun einen Server finden, der Sinusbot Instanz heißt. Die IP Adresse und den Port kannst du direkt unten in den Information finden. (Siehe Bild)</p><br><img src="https://cdn.jonas-budde.de/screenshots/Oyv95DqigRkn7W0iYLILhQzMsqWhFGlS.png"><br>
